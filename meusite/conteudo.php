<center><h2 style="font-size:30px;"><font face="Courier New">Travis Scott lançou seu aguardado álbum após 4 anos de espera.</font></h2></center>

<p style="text-align: justify; font-family:arial;font-size:120%;"><strong><font color="red" >UTOPIA</font></strong> é o quarto álbum de estúdio do rapper americano <strong>Travis Scott</strong>.O mundo da música recebe com grande expectativa o lançamento do novo álbum de Travis Scott, chamado “UTOPIA”. Desde seu terceiro álbum, “Astroworld”, lançado em 2018,que foi certificado com disco de platina e recebeu indicação ao Grammy Awards de 2019 como Melhor Álbum de Rap  ,o rapper não lançava um novo projeto. Agora, quase quatro anos depois, <strong>Travis Scott</strong> está pronto para repetir o sucesso com seu tão aguardado quarto álbum, “UTOPIA”.</p>

<p style="text-align: justify; font-family:arial;font-size:120%;">Junto com o lançamento do álbum, <strong>Travis Scott</strong> também lançou o filme “Circus Maximus”, mesmo nome de uma das faixas de seu novo álbum.O novo disco de Travis Scott conta com nomes como The Weeknd, Drake, Beyoncé, Playboi Carti e mais.</p>

<p style="text-align: justify; font-family:arial;font-size:120%;">A ansiedade é grande entre os fãs, que aguardam com entusiasmo para ouvir o que Travis Scott preparou após tanto tempo de dedicação à criação deste álbum. A espera está prestes a acabar, e a expectativa é de que <font color ="red">“UTOPIA”</font> seja mais um grande sucesso na carreira do renomado rapper.</p>

