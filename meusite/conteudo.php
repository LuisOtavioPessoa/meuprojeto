 <center><h2 style="font-size:30px;"><font face="Courier New">Lil Uzi Vert coloca 18 músicas do álbum ‘Pink Tape’ na Billboard Hot 100</font></h2></center>

 <p style="text-align: justify;font-family:arial;font-size:120%;"><strong><font color="red">Pink Tape </strong></font> é o terceiro álbum de estúdio do rapper e cantor americano <strong>Lil Uzi Vert</strong> . Foi lançado pela Generation Now e Atlantic Records em 30 de junho de 2023.No disco, ele exibe uma incrível visão e senso de moda somado ao som de “trap futurista”, apresentando uma voz altamente autêntica e dinâmica. </p>

 <p style="text-align: justify; font-family:arial;font-size:120%;">O rapper <strong>Lil Uzi Vert</strong> segue atingindo níveis extraordinários na última lista da Billboard Hot 100, ao se tornar apenas o 14º artista, nos quase 65 anos de história do gráfico, a registrar mais de 100 entradas nas paradas da revista ao longo de sua carreira. Com os novos lançamentos, o cantor agora detém 106 músicas incluídas no ranking. </p>

 <p style="text-align: justify; font-family:arial;font-size:120%;"><strong>Lil Uzi</strong> estreou seu álbum “<font color="red">Pink Tape</font>” com 18 músicas presentes no Hot 100. O álbum estreou em primeiro lugar na Billboard 200 com 167.000 unidades de álbum equivalentes em sua semana de abertura, de acordo com Luminate. Isso deu a Uzi seu terceiro líder na Billboard 200, depois de “Luv Is Rage 2”, de 2017, e “Eternal Atake”, de 2020.</p>

